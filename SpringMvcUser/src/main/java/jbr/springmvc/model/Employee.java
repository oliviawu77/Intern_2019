package jbr.springmvc.model;

public class Employee {

	private String id;
	private String job_grade;
	private String job_position;
	private String work_shift;
	private String exercise_date;
	private String exercise_week;
	private String step;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getJob_grade() {
		return job_grade;
	}
	public void setJob_grade(String job_grade) {
		this.job_grade = job_grade;
	}
	public String getJob_position() {
		return job_position;
	}
	public void setJob_position(String job_position) {
		this.job_position = job_position;
	}
	public String getWork_shift() {
		return work_shift;
	}
	public void setWork_shift(String work_shift) {
		this.work_shift = work_shift;
	}
	public String getExercise_date() {
		return exercise_date;
	}
	public void setExercise_date(String exercise_date) {
		this.exercise_date = exercise_date;
	}
	public String getExercise_week() {
		return exercise_week;
	}
	public void setExercise_week(String exercise_week) {
		this.exercise_week = exercise_week;
	}
	public String getStep() {
		return step;
	}
	public void setStep(String step) {
		this.step = step;
	}
	
}
